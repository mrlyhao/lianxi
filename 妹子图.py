def get_html(url):
    try:
        web_data = requests.get(url)
        web_data.encoding = 'gb2312'
        html = etree.HTML(web_data.text)
        return html
    except:
        return ''

def get_url(url):#获取页面的xpath解析
    try:
        html = get_html(url)
        meiziurl = html.xpath('//li[@class = "wp-item"]/div/div/a/@href')
        meiziname = html.xpath('//li[@class = "wp-item"]/div/h3/a/b/text()')
        return meiziurl,meiziname
    except:
        return ''

def get_mmzhaopian(meiziurl,meiziname,referer):#解析页面并下载
    try:
        html = get_html(meiziurl)
        tupianurls = html.xpath('//div[@id="picture"]/p/img/@src')#抓取图片链接
        filename = ('D:/MM/'+meiziname)
        exists = os.path.exists(filename)
        if not exists:#检查文件夹是否存在，若不存在则新建文件夹
            os.makedirs(filename)
        headers = {'Referer':referer,
                   'User-Agent':GetUserAgent()}#由于有防盗链设置随机请求头，和referer
        for tupianurl in tupianurls:
            tupianname = tupianurl.split('/')[-1]#获取每个图片名称
            picture = requests.get(tupianurl,headers=headers)
            print(headers)
            path = filename+'/' +tupianname
            with open(path,'wb') as f:
                f.write(picture.content)#下载图片
    except:
        return ''

