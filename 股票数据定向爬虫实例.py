def getHTMLList(url,code = 'utf-8'):
    try:
        headers = {'User-Agent':GetUserAgent()}
        r = requests.get(url,timeout = 30,headers=headers)
        r.encoding = code
        r.raise_for_status()
        return r.text
    except:
        return  ''

def get_StockList(stockURL):
    list = []
    html = getHTMLList(stockURL,'GB2312')
    soup = etree.HTML(html)
    a= soup.xpath('//div[@id="quotesearch"]/ul/li/a[@target="_blank"]/@href')
    for i in a:
        try:
            list.append(i.split('/')[-1][:-5])
        except:
            continue
    return list
def get_Stocksinfo(stock):
    start_url = 'https://gupiao.baidu.com/stock/'
    sotck_url = start_url+stock+'.html'
    html = getHTMLList(sotck_url)
    soup = etree.HTML(html)
    stockInfo = soup.xpath('//div[@class="bets-content"]//dd/text()')
    name = soup.xpath('//h1/a/text()')[0].strip().replace('(','')
    stockInfo.insert(0,stock)
    stockInfo.insert(0,name)
    print(stockInfo)
    return stockInfo

